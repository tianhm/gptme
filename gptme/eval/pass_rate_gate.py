"""Natural pass-rate gating for eval lesson injection (Phase 3 of crossover-effect work).

Pre-computed per-(model, eval) pass rates from holdout data tell us where lessons
help, hurt, or are neutral. This module loads that data and exposes a per-pair
gate recommendation that the eval runner consults before deciding whether to
inject lessons.

Data source format::

    {
      "lookup": {
        "<model>": {
          "<eval-name>": {
            "baseline": 1.0, "baseline_n": 6,
            "holdout":  0.83, "holdout_n":  6,
            "delta": 0.17, "effect": "lessons_help",
            "gate_recommendation": "inject" | "suppress" | "default"
          }, ...
        }, ...
      }
    }

Generated by Bob's ``scripts/compute-natural-pass-rates.py``. Pointed to via
``GPTME_EVAL_PASS_RATE_GATE_FILE`` (or passed explicitly to ``load_pass_rate_data``).
Gate is opt-in: missing file => no gating (caller falls back to existing logic).
"""

import json
import logging
import os
from pathlib import Path
from typing import Literal

logger = logging.getLogger(__name__)

GateDecision = Literal["inject", "suppress", "default"]

ENV_VAR = "GPTME_EVAL_PASS_RATE_GATE_FILE"


def load_pass_rate_data(path: Path | str | None = None) -> dict | None:
    """Load natural pass-rate gate data from a JSON file.

    Lookup order: explicit path arg, then ``$GPTME_EVAL_PASS_RATE_GATE_FILE``.
    Returns ``None`` if no path is configured or the file does not exist.
    Raises ``ValueError`` for malformed JSON so misconfigurations fail loudly.
    """
    from_env = False
    if path is None:
        env = os.environ.get(ENV_VAR)
        if not env:
            return None
        path = env
        from_env = True
    p = Path(path)
    if not p.is_file():
        if from_env:
            logger.warning(
                "Pass-rate gate file not found (set via %s): %s — gating disabled",
                ENV_VAR,
                p,
            )
        else:
            logger.debug("Pass-rate gate file not found: %s", p)
        return None
    try:
        data = json.loads(p.read_text())
    except json.JSONDecodeError as e:
        raise ValueError(f"Invalid pass-rate gate JSON at {p}: {e}") from e
    if "lookup" not in data or not isinstance(data["lookup"], dict):
        raise ValueError(f"Pass-rate gate JSON at {p} missing top-level 'lookup' dict")
    return data


_ROUTER_PREFIXES = ("openrouter/",)


def _normalize_model_key(model: str, lookup: dict) -> str:
    """Return the best matching key for *model* in *lookup*.

    Tries an exact match first, then retries after stripping known router
    prefixes (e.g. ``openrouter/anthropic/X`` → ``anthropic/X``). This
    handles the case where gate data was collected via the direct API but
    evals are executed via an OpenRouter backend.
    """
    if model in lookup:
        return model
    for prefix in _ROUTER_PREFIXES:
        if model.startswith(prefix):
            candidate = model[len(prefix) :]
            if candidate in lookup:
                logger.debug(
                    "Pass-rate gate: matched '%s' via normalized key '%s'",
                    model,
                    candidate,
                )
                return candidate
    return model  # return original; lookup miss handled by caller


def get_gate_recommendation(
    model: str,
    eval_name: str,
    data: dict | None,
) -> GateDecision:
    """Look up the gate decision for a (model, eval) pair.

    Returns ``"default"`` when no data is loaded or the pair is not present —
    callers should treat that as "no override; use existing logic".

    Normalizes the model key by stripping known router prefixes when an exact
    match is absent (e.g. ``openrouter/anthropic/X`` → ``anthropic/X``).
    """
    if not data:
        return "default"
    lookup = data.get("lookup", {})
    key = _normalize_model_key(model, lookup)
    rec = lookup.get(key, {}).get(eval_name, {}).get("gate_recommendation")
    if rec in ("inject", "suppress"):
        return rec  # type: ignore[return-value]
    return "default"


def apply_gate(
    *,
    model: str,
    eval_name: str,
    no_lessons: bool,
    data: dict | None,
) -> tuple[bool, GateDecision]:
    """Resolve the effective ``no_lessons`` flag using the natural pass-rate gate.

    Returns ``(effective_no_lessons, decision)``. The decision is returned so
    callers can log the reasoning. ``"default"`` means no override; the input
    ``no_lessons`` is returned unchanged.
    """
    decision = get_gate_recommendation(model, eval_name, data)
    if decision == "suppress":
        return True, decision
    if decision == "inject":
        return False, decision
    return no_lessons, decision
